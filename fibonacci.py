
# --- 実行部分 ---
# 要素数（最大インデックス）を入力
n = 7 

# 1. 指定された要素数(n + 1個)の配列を作成（最初はすべて0にしておく）
F = [0] * (n + 1)
    
# 2. 初期値の設定
F[0] = 0
F[1] = 1
    
# 3. ループ処理 (iを2からnまで繰り返す)
for i in range(2, n + 1):
# 直前の2つの項の和を現在の項に代入
    F[i] = F[i - 1] + F[i - 2]
        

# 出力（画像の例に合わせた形式）
print(n)
for value in F:
    print(value)

