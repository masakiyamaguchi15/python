# 最初の数値の入力を促す
num1 = float(input("最初の数値を入力してください: "))

# 2番目の数値の入力を促す
num2 = float(input("2番目の数値を入力してください: "))

# 3番目の数値の入力を促す
num3 = float(input("3番目の数値を入力してください: "))

# 合計を計算する
total = num1 + num2 + num3

# 平均を計算する
average = total / 3

# 合計を出力する
print("合計は:", total)

# 平均を出力する
print("平均は:", average)
