成績表 = [50, 96, 89, 72, 16, 67, 22, 34, 57, 98, 100, 66]

最大値 = 成績表[0]
最小値 = 成績表[0]

print("最大値:", 最大値)
print("最小値:", 最小値)


要素数 = len(成績表)

for i in range(要素数):
    if 成績表[i] > 最大値:
        最大値 = 成績表[i]
        print("最大値:", 最大値)  
    if 成績表[i] < 最小値:
        最小値 = 成績表[i]
        print("最小値:", 最小値)


print("最大値:", 最大値)
print("最小値:", 最小値)

